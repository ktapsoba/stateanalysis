package example;

public class Unit {
	int id;
	String number;
	int bedrooms;
	int batchrooms;
	int livingrooms;
	int diningrooms;
	int kitchen;
	boolean furnished;
	boolean hasBalcony;
	double area;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public int getBedrooms() {
		return bedrooms;
	}
	public void setBedrooms(int bedrooms) {
		this.bedrooms = bedrooms;
	}
	public int getBatchrooms() {
		return batchrooms;
	}
	public void setBatchrooms(int batchrooms) {
		this.batchrooms = batchrooms;
	}
	public int getLivingrooms() {
		return livingrooms;
	}
	public void setLivingrooms(int livingrooms) {
		this.livingrooms = livingrooms;
	}
	public int getDiningrooms() {
		return diningrooms;
	}
	public void setDiningrooms(int diningrooms) {
		this.diningrooms = diningrooms;
	}
	public int getKitchen() {
		return kitchen;
	}
	public void setKitchen(int kitchen) {
		this.kitchen = kitchen;
	}
	public boolean isFurnished() {
		return furnished;
	}
	public void setFurnished(boolean furnished) {
		this.furnished = furnished;
	}
	public boolean isHasBalcony() {
		return hasBalcony;
	}
	public void setHasBalcony(boolean hasBalcony) {
		this.hasBalcony = hasBalcony;
	}
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	
	

}
